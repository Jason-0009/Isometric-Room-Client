/**
 * Enumerates the possible directions for a wall.
 */
enum WallDirections {
    /**
     * Represents a wall that exists on both sides.
     */
    BOTH,
  
    /**
     * Represents a wall that exists on the left side.
     */
    LEFT,
  
    /**
     * Represents a wall that exists on the right side.
     */
    RIGHT,
  }
  
  export default WallDirections;
  